//add
function add(a, b) {
  return Math.abs(a + b);
}
console.log(add(3, 2));

//subtract
function subtract(a, b) {
  return Math.abs(a - b);
}
console.log(subtract(12, 6));
//multiply
function multiply(a, b) {
  return Math.abs(a * b);
}
console.log(multiply(5, 3));

//divide
function divide(a, b) {
  return Math.abs(a / b);
}
console.log(divide(8, 4));
